# Operators in Python

## Arithmetic Operators

#+  -  *  /  %  **

a = 5
b = 10

print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a % b)
print(a ** b)

#---

## Relational Operators

#==  !=  >  <  >=  <=

a = 10
b = 20

print(a == b)
print(a != b)
print(a > b)
print(a < b)

#---

## Assignment Operators

#=  +=  -=  *=  /=  %=  **=

x = 10
x += 5
x -= 3
x *= 2

#---

## Logical Operators

#and, or, not

x = 10
y = 20
z = 5

print(x > z and y > x)
print(x > y or y > z)
print(not (x > y))
